        # breath –∑–º—ñ–Ω—é—î—Ç—å—Å—è –≤—ñ–¥ 0.95 –¥–æ 1.05 (–µ—Ñ–µ–∫—Ç –ø—É–ª—å—Å–∞—Ü—ñ—ó)
        breath = 1.0 + 0.05 * np.sin(phase)
        
