import streamlit as st
import numpy as np
import matplotlib.pyplot as plt
from matplotlib import cm
import io

# 1. –ù–ê–õ–ê–®–¢–£–í–ê–ù–ù–Ø
st.set_page_config(page_title="–ú–∞–Ω–¥–∞–ª–∞ –æ—Å–æ–±–∏—Å—Ç–æ—Å—Ç—ñ", layout="wide")

st.title("üé® –¶–∏—Ñ—Ä–æ–≤–∞ –º–∞–Ω–¥–∞–ª–∞ –æ—Å–æ–±–∏—Å—Ç–æ—Å—Ç—ñ")
st.write("### –ú–∞—Ç–µ–º–∞—Ç–∏—á–Ω–æ-–º–∏—Å—Ç–µ—Ü—å–∫–∏–π –ø—Ä–æ—î–∫—Ç")
st.markdown("---")

# 2. –í–ö–õ–ê–î–ö–ò
tab1, tab2 = st.tabs(["üöÄ –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä –º–∞–Ω–¥–∞–ª–∏", "üìú –ú–∞—Ç–µ–º–∞—Ç–∏—á–Ω–µ –æ–±“ë—Ä—É–Ω—Ç—É–≤–∞–Ω–Ω—è"])

with tab2:
    st.header("–ú–∞—Ç–µ–º–∞—Ç–∏—á–Ω–∞ –º–æ–¥–µ–ª—å")
    st.write("–ü—Ä–æ—Å—Ç—ñ—Ä –Ω–æ—Ä–º–æ–≤–∞–Ω–æ ($R \approx 1.5$). –í—Å—ñ –º–∞–Ω–¥–∞–ª–∏ –º–∞—é—Ç—å –æ–¥–Ω–∞–∫–æ–≤–∏–π —Ä–æ–∑–º—ñ—Ä, –∑–º—ñ–Ω—é—î—Ç—å—Å—è –ª–∏—à–µ —Å—Ç—Ä—É–∫—Ç—É—Ä–∞.")

    # --- 1. –°–û–ù ---
    st.subheader("1. –Ø–¥—Ä–æ (–°–æ–Ω)")
    st.write("–¶–µ–Ω—Ç—Ä –º–∞–Ω–¥–∞–ª–∏ (–æ—Ç–≤—ñ—Ä) –∑–∞–ª–µ–∂–∏—Ç—å –≤—ñ–¥ –≥–æ–¥–∏–Ω —Å–Ω—É ($S$).")
    st.latex(r"r_{hole} = R_{core} \cdot \left(1 - \frac{S}{12}\right)")

    # --- 2. –î–ï–ù–¨ –ù–ê–†–û–î–ñ–ï–ù–ù–Ø ---
    st.subheader("2. –ì–∞—Ä–º–æ–Ω—ñ—á–Ω–∞ –∑—ñ—Ä–∫–∞ (–î–µ–Ω—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è)")
    st.write("–î–µ–Ω—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è ($d$) –≤–∏–∑–Ω–∞—á–∞—î –∫—ñ–ª—å–∫—ñ—Å—Ç—å –≤–µ—Ä—à–∏–Ω –≤–Ω—É—Ç—Ä—ñ—à–Ω—å–æ—ó –∑—ñ—Ä–∫–∏ (–ï–ø—ñ—Ç—Ä–æ—Ö–æ—ó–¥–∏).")
    st.latex(r"N_{star} = d")

    # --- 3. –ü–ï–õ–Æ–°–¢–ö–ò ---
    st.subheader("3. –ï–º–æ—Ü—ñ–π–Ω–∏–π —à–∞—Ä (–ú—ñ—Å—è—Ü—å —Ç–∞ –í–ø–µ–≤–Ω–µ–Ω—ñ—Å—Ç—å)")
    st.write("–§–æ—Ä–º–∞ –∑–∞–ª–µ–∂–∏—Ç—å –≤—ñ–¥ –º—ñ—Å—è—Ü—è ($n$), –≥–æ—Å—Ç—Ä–æ—Ç–∞ ‚Äî –≤—ñ–¥ –í–ø–µ–≤–Ω–µ–Ω–æ—Å—Ç—ñ ($E$).")
    st.latex(r"r_{rose} = |\cos(\frac{n}{2}\theta)|^p")

    # --- 4. –°–ü–Ü–†–ê–õ–Ü (–ù–û–í–ê –õ–û–ì–Ü–ö–ê) ---
    st.subheader("4. –ü–æ–ª–µ –∂–∏—Ç—Ç—î–≤–æ–≥–æ —à–ª—è—Ö—É (–í—ñ–∫ —Ç–∞ –ï–Ω–µ—Ä–≥—ñ—è)")
    st.write("–ú–∏ –∑–º—ñ–Ω–∏–ª–∏ –ª–æ–≥—ñ–∫—É, —â–æ–± —É–Ω–∏–∫–Ω—É—Ç–∏ –≤—ñ–∑—É–∞–ª—å–Ω–æ–≥–æ —à—É–º—É:")
    st.write("* **–ö—ñ–ª—å–∫—ñ—Å—Ç—å —Ä—É–∫–∞–≤—ñ–≤ —Å–ø—ñ—Ä–∞–ª—ñ** –≤–∏–∑–Ω–∞—á–∞—î—Ç—å—Å—è –ï–Ω–µ—Ä–≥—ñ—î—é ($T$). –¶–µ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –∂–∏—Ç—Ç—î–≤–∏—Ö –≤–µ–∫—Ç–æ—Ä—ñ–≤.")
    st.write("* **–î–æ–≤–∂–∏–Ω–∞ —Ä—É–∫–∞–≤–∞** –≤–∏–∑–Ω–∞—á–∞—î—Ç—å—Å—è –í—ñ–∫–æ–º ($A$). –¶–µ –¥–æ—Å–≤—ñ–¥.")
    st.latex(r"L_{spiral} \propto A, \quad N_{arms} \propto T")
    st.write("–ù–∞–ø—Ä—è–º–æ–∫ –∑–∞–∫—Ä—É—á—É–≤–∞–Ω–Ω—è –∑–∞–ª–µ–∂–∏—Ç—å –≤—ñ–¥ –°—Ç–∞—Ç—ñ ($G$).")

    # --- 5. –ó–†–Ü–°–¢ ---
    st.subheader("5. –ó–∞—Ö–∏—Å–Ω–∞ –º–µ–∂–∞ (–ó—Ä—ñ—Å—Ç)")
    st.write("–ó—Ä—ñ—Å—Ç ($H$) –≤–∏–∑–Ω–∞—á–∞—î **–∫—ñ–ª—å–∫—ñ—Å—Ç—å –∑—É–±—Ü—ñ–≤** –Ω–∞ –∑–æ–≤–Ω—ñ—à–Ω—å–æ–º—É –∫–æ–Ω—Ç—É—Ä—ñ.")
    st.latex(r"N_{peaks} = \frac{H}{10}")

    # --- 6. –¢–ï–ú–ü–ï–†–ê–ú–ï–ù–¢ ---
    st.subheader("6. –¢–µ–º–ø–µ—Ä–∞–º–µ–Ω—Ç")
    st.write("–í–∏–∑–Ω–∞—á–∞—î —Å—Ç–∏–ª—å —à—Ç—Ä–∏—Ö–∞ (—Ç–æ–≤—â–∏–Ω–∞, –ø—Ä–æ–∑–æ—Ä—ñ—Å—Ç—å).")

with tab1:
    # –ü–ê–ù–ï–õ–¨ –ö–ï–†–£–í–ê–ù–ù–Ø
    st.sidebar.header("üìã –í—Ö—ñ–¥–Ω—ñ –ø–∞—Ä–∞–º–µ—Ç—Ä–∏")
    with st.sidebar:
        # –î–µ–Ω—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è
        d = st.number_input("–î–µ–Ω—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è (–ö—ñ–ª—å–∫—ñ—Å—Ç—å –ø—Ä–æ–º–µ–Ω—ñ–≤ –∑—ñ—Ä–∫–∏)", 1, 31, 15)
        n = st.number_input("–ú—ñ—Å—è—Ü—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è (–§–æ—Ä–º–∞ –ø–µ–ª—é—Å—Ç–æ–∫)", 1, 12, 6)
        
        # –ó—Ä—ñ—Å—Ç
        H = st.slider("–ó—Ä—ñ—Å—Ç (—Å–º) -> –ö—ñ–ª—å–∫—ñ—Å—Ç—å –≤–µ—Ä—à–∏–Ω –º–µ–∂—ñ", 100, 220, 170)
        
        # –ï–ù–ï–†–ì–Ü–Ø -> –ö—ñ–ª—å–∫—ñ—Å—Ç—å –ª—ñ–Ω—ñ–π (—â–æ–± –Ω–µ –±—É–ª–æ –∫–∞—à—ñ)
        T = st.slider("–ï–Ω–µ—Ä–≥—ñ—è -> –ö—ñ–ª—å–∫—ñ—Å—Ç—å —Ä—É–∫–∞–≤—ñ–≤ —Å–ø—ñ—Ä–∞–ª—ñ", 3, 12, 5)
        
        # –í–Ü–ö -> –î–æ–≤–∂–∏–Ω–∞ –ª—ñ–Ω—ñ–π
        A = st.slider("–í—ñ–∫ -> –î–æ–≤–∂–∏–Ω–∞ –∑–∞–∫—Ä—É—á—É–≤–∞–Ω–Ω—è", 10, 100, 45)
        
        S = st.slider("–ì–æ–¥–∏–Ω —Å–Ω—É (–©—ñ–ª—å–Ω—ñ—Å—Ç—å —Ü–µ–Ω—Ç—Ä—É)", 0, 12, 8)
        E = st.slider("–í–ø–µ–≤–Ω–µ–Ω—ñ—Å—Ç—å (–ì–æ—Å—Ç—Ä–æ—Ç–∞ –ø–µ–ª—é—Å—Ç–æ–∫)", 0, 10, 5)
        
        st.markdown("---")
        G = st.radio("–°—Ç–∞—Ç—å", options=[1, -1], format_func=lambda x: "–ß–æ–ª–æ–≤—ñ—á–∞" if x == 1 else "–ñ—ñ–Ω–æ—á–∞")
        temp = st.selectbox("–¢–µ–º–ø–µ—Ä–∞–º–µ–Ω—Ç", options=["–°–∞–Ω–≥–≤—ñ–Ω—ñ–∫", "–•–æ–ª–µ—Ä–∏–∫", "–§–ª–µ–≥–º–∞—Ç–∏–∫", "–ú–µ–ª–∞–Ω—Ö–æ–ª—ñ–∫"])
        eye_choice = st.selectbox("–ö–æ–ª—ñ—Ä –æ—á–µ–π", options=[1, 2, 3, 4], 
                                  format_func=lambda x: {1:"–ë–ª–∞–∫–∏—Ç–Ω—ñ", 2:"–ó–µ–ª–µ–Ω—ñ", 3:"–ö–∞—Ä—ñ", 4:"–Ø–Ω—Ç–∞—Ä–Ω—ñ"}[x])

    # –ì–ï–ù–ï–†–ê–¶–Ü–Ø
    def generate_mandala():
        style_map = {
            "–°–∞–Ω–≥–≤—ñ–Ω—ñ–∫": {"lw": 1.5, "alpha": 0.8},
            "–•–æ–ª–µ—Ä–∏–∫":   {"lw": 2.5, "alpha": 1.0},
            "–§–ª–µ–≥–º–∞—Ç–∏–∫": {"lw": 4.0, "alpha": 0.4},
            "–ú–µ–ª–∞–Ω—Ö–æ–ª—ñ–∫": {"lw": 1.0, "alpha": 0.7}
        }
        s = style_map[temp]
        selected_cmap = {1: cm.winter, 2: cm.summer, 3: cm.autumn, 4: cm.spring}.get(eye_choice, cm.plasma)
        
        t = np.linspace(0, 2 * np.pi, 4000)
        fig = plt.figure(figsize=(6, 6), facecolor='black')
        ax = plt.subplot(111, projection='polar')
        ax.set_facecolor('black')
        
        SCALE = 0.12 # –ì–ª–æ–±–∞–ª—å–Ω–∏–π –º–∞—Å—à—Ç–∞–±
        
        # === 1. –Ø–î–†–û (–°–û–ù S) ===
        R_core = 2.0 * SCALE
        r_hole = R_core * max(0.0, 1 - (S / 12.0))
        
        ax.fill_between(t, r_hole, R_core, color=selected_cmap(0.9), alpha=s["alpha"]+0.1)
        ax.plot(t, np.full_like(t, r_hole), color='white', linewidth=0.5, alpha=0.5)

        # === 2. –ó–Ü–†–ö–ê (–î–ï–ù–¨ –ù–ê–†–û–î–ñ–ï–ù–ù–Ø d) ===
        R_layer2 = R_core + 0.3 * SCALE
        r_star = R_layer2 + 0.25 * SCALE * np.cos(d * t)
        ax.plot(t, r_star, color=selected_cmap(0.7), linewidth=s["lw"]*0.8, alpha=0.8)

        # === 3. –ü–ï–õ–Æ–°–¢–ö–ò (–ú–Ü–°–Ø–¶–¨ n, –í–ü–ï–í–ù–ï–ù–Ü–°–¢–¨ E) ===
        R_rose_base = R_layer2 + 0.5 * SCALE
        e_val = (11 - E) / 2
        r_rose = R_rose_base + (np.abs(np.cos(n/2 * t)))**e_val * 2.5 * SCALE
        
        ax.fill(t, r_rose, color=selected_cmap(0.3), alpha=0.3)
        ax.plot(t, r_rose, color=selected_cmap(0.4), linewidth=s["lw"], alpha=0.9)

        # === 4. –°–ü–Ü–†–ê–õ–Ü (–ù–û–í–ê –ß–ò–°–¢–ê –õ–û–ì–Ü–ö–ê) ===
        max_r_rose = r_rose.max()
        
        # –ö—ñ–ª—å–∫—ñ—Å—Ç—å —Ä—É–∫–∞–≤—ñ–≤ = –ï–ù–ï–†–ì–Ü–Ø (T). –¶–µ –º–∞–ª–æ –ª—ñ–Ω—ñ–π (3-12), —Ç–æ–º—É –Ω–µ –±—É–¥–µ –±—Ä—É–¥—É.
        num_arms = max(3, int(T))
        
        for i in range(num_arms):
            # –ö—É—Ç –∑—Å—É–≤—É –¥–ª—è —Å–∏–º–µ—Ç—Ä—ñ—ó
            angle_shift = (2 * np.pi / num_arms) * i
            
            # –î–æ–≤–∂–∏–Ω–∞ —Å–ø—ñ—Ä–∞–ª—ñ = –í–Ü–ö (A). 
            # 10 —Ä–æ–∫—ñ–≤ = –∫–æ—Ä–æ—Ç–∫–∞ —Ä–∏—Å–∫–∞. 100 —Ä–æ–∫—ñ–≤ = –¥–æ–≤–≥–∏–π –æ–±–µ—Ä—Ç.
            spiral_len = (A / 100.0) * (np.pi * 3) # –ú–∞–∫—Å 1.5 –æ–±–µ—Ä—Ç–∏
            
            # –¢–æ—á–∫–∏ –¥–ª—è –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ—ó —Å–ø—ñ—Ä–∞–ª—ñ
            t_sp = np.linspace(0, spiral_len, 200)
            
            # –†–∞–¥—ñ—É—Å —Ä–æ—Å—Ç–µ –ø–ª–∞–≤–Ω–æ
            r_sp = max_r_rose + (t_sp * 0.5 * SCALE)
            
            # –û–±–µ—Ä—Ç–∞–Ω–Ω—è: G (—Å—Ç–∞—Ç—å) –≤–∏–∑–Ω–∞—á–∞—î –Ω–∞–ø—Ä—è–º–æ–∫
            theta_sp = angle_shift + (G * t_sp)
            
            ax.plot(theta_sp, r_sp, color=selected_cmap(0.6 + i/num_arms/3), 
                    linewidth=s["lw"]*0.8, alpha=0.8)

        # === 5. –ú–ï–ñ–ê (–ó–†–Ü–°–¢ H, –°–¢–ê–¢–¨ G) ===
        # –ö—ñ–ª—å–∫—ñ—Å—Ç—å –∑—É–±—Ü—ñ–≤ = –ó—Ä—ñ—Å—Ç / 10
        border_freq = int(H / 10)
        
        # –ú–µ–∂–∞ –∑–∞–≤–∂–¥–∏ —Ç—Ä–æ—Ö–∏ –¥–∞–ª—ñ –∑–∞ –Ω–∞–π–¥–æ–≤—à—É –º–æ–∂–ª–∏–≤—É —Å–ø—ñ—Ä–∞–ª—å
        r_border_base = max_r_rose + 2.5 * SCALE
        
        # –§–æ—Ä–º–∞: –ß–æ–ª–æ–≤—ñ–∫–∏ –≥–æ—Å—Ç—Ä—ñ, –ñ—ñ–Ω–∫–∏ –º'—è–∫—ñ
        p_shape = 0.5 if G == 1 else 1.5
        
        crown = (np.abs(np.sin(border_freq * t)))**p_shape
        r_border = r_border_base + (0.8 * SCALE * crown)
        
        ax.plot(t, r_border, color=selected_cmap(0.9), linewidth=s["lw"]*1.5)

        # –§—ñ–∫—Å–∞—Ü—ñ—è
        ax.set_ylim(0, 1.45)
        ax.set_axis_off()
        return fig

    col1, col2 = st.columns([1, 2])
    with col2:
        fig = generate_mandala()
        st.pyplot(fig)
        
        buf = io.BytesIO()
        fig.savefig(buf, format="png", facecolor='black', dpi=300)
        st.download_button("üì• –ó–∞–≤–∞–Ω—Ç–∞–∂–∏—Ç–∏ PNG", buf.getvalue(), "mandala.png", "image/png")
