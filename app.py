import streamlit as st
import numpy as np
import matplotlib.pyplot as plt
from matplotlib import cm
import io

# 1. –ù–ê–õ–ê–®–¢–£–í–ê–ù–ù–Ø
st.set_page_config(page_title="–ú–∞–Ω–¥–∞–ª–∞ –æ—Å–æ–±–∏—Å—Ç–æ—Å—Ç—ñ", layout="wide")

st.title("üé® –¶–∏—Ñ—Ä–æ–≤–∞ –º–∞–Ω–¥–∞–ª–∞ –æ—Å–æ–±–∏—Å—Ç–æ—Å—Ç—ñ")
st.write("### –ú–∞—Ç–µ–º–∞—Ç–∏—á–Ω–æ-–º–∏—Å—Ç–µ—Ü—å–∫–∏–π –ø—Ä–æ—î–∫—Ç")
st.markdown("---")

# 2. –í–ö–õ–ê–î–ö–ò
tab1, tab2 = st.tabs(["üöÄ –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä", "üìú –ù–∞—É–∫–æ–≤–µ –æ–±“ë—Ä—É–Ω—Ç—É–≤–∞–Ω–Ω—è"])

with tab2:
    st.header("–ú–∞—Ç–µ–º–∞—Ç–∏—á–Ω–∞ –º–æ–¥–µ–ª—å")
    st.write("–ü—Ä–æ—Å—Ç—ñ—Ä –Ω–æ—Ä–º–æ–≤–∞–Ω–æ ($R \approx 1.45$).")

    # --- 1. –°–û–ù ---
    st.subheader("1. –Ø–¥—Ä–æ (–°–æ–Ω)")
    st.latex(r"r_{hole} = R_{core} \cdot \left(1 - \frac{S}{12}\right)")
    st.write("–î–µ—Ñ—ñ—Ü–∏—Ç —Å–Ω—É —Å—Ç–≤–æ—Ä—é—î –ø–æ—Ä–æ–∂–Ω–µ—á—É –≤ —Ü–µ–Ω—Ç—Ä—ñ.")

    # --- 2. –î–ï–ù–¨ –ù–ê–†–û–î–ñ–ï–ù–ù–Ø ---
    st.subheader("2. –ì–∞—Ä–º–æ–Ω—ñ—á–Ω–∞ –∑—ñ—Ä–∫–∞ (–î–µ–Ω—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è)")
    st.write("–î–µ–Ω—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è ($d$) –≤–∏–∑–Ω–∞—á–∞—î –∫—ñ–ª—å–∫—ñ—Å—Ç—å –≤–µ—Ä—à–∏–Ω –≤–Ω—É—Ç—Ä—ñ—à–Ω—å–æ—ó –∑—ñ—Ä–∫–∏.")
    st.latex(r"N_{star} = d")

    # --- 3. –ü–ï–õ–Æ–°–¢–ö–ò ---
    st.subheader("3. –ï–º–æ—Ü—ñ–π–Ω–∏–π —à–∞—Ä (–ú—ñ—Å—è—Ü—å —Ç–∞ –í–ø–µ–≤–Ω–µ–Ω—ñ—Å—Ç—å)")
    st.write("–ú—ñ—Å—è—Ü—å ($n$) –∑–∞–¥–∞—î —Ñ–æ—Ä–º—É, –í–ø–µ–≤–Ω–µ–Ω—ñ—Å—Ç—å ($E$) ‚Äî –≥–æ—Å—Ç—Ä–æ—Ç—É.")
    st.latex(r"r_{rose} = |\cos(\frac{n}{2}\theta)|^p")

    # --- 4. –°–ü–Ü–†–ê–õ–Ü (–ù–û–í–ï - –ö–õ–ê–°–¢–ï–†–ò–ó–ê–¶–Ü–Ø) ---
    st.subheader("4. –ü–æ—Ç–æ–∫–∏ –∂–∏—Ç—Ç—è (–í—ñ–∫ —Ç–∞ –ï–Ω–µ—Ä–≥—ñ—è)")
    st.write("–í—ñ–∫ ($A$) ‚Äî —Ü–µ –∑–∞–≥–∞–ª—å–Ω–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –ª—ñ–Ω—ñ–π ('–Ω–∏—Ç–æ–∫ –¥–æ–ª—ñ'). –©–æ–± —É–Ω–∏–∫–Ω—É—Ç–∏ —Ö–∞–æ—Å—É, –≤–æ–Ω–∏ –≥—Ä—É–ø—É—é—Ç—å—Å—è –≤ **–ï–Ω–µ—Ä–≥–µ—Ç–∏—á–Ω—ñ –ø–æ—Ç–æ–∫–∏**.")
    st.write("**–ï–Ω–µ—Ä–≥—ñ—è ($T$)** –≤–∏–∑–Ω–∞—á–∞—î –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Ü–∏—Ö –ø–æ—Ç–æ–∫—ñ–≤ (—Ä—É–∫–∞–≤—ñ–≤).")
    st.latex(r"\theta_{rot} = \frac{2\pi}{T} \cdot (i \mod T) + \delta")
    st.write("–°—Ç–∞—Ä—à–∞ –ª—é–¥–∏–Ω–∞ –º–∞—î –±—ñ–ª—å—à –Ω–∞—Å–∏—á–µ–Ω—ñ, '—Ç–æ–≤—Å—Ç—ñ' –ø–æ—Ç–æ–∫–∏ –ª—ñ–Ω—ñ–π, –º–æ–ª–æ–¥–∞ ‚Äî —Ç–æ–Ω–∫—ñ.")

    # --- 5. –ó–†–Ü–°–¢ ---
    st.subheader("5. –ó–∞—Ö–∏—Å–Ω–∞ –º–µ–∂–∞ (–ó—Ä—ñ—Å—Ç)")
    st.write("–ó—Ä—ñ—Å—Ç ($H$) –≤–∏–∑–Ω–∞—á–∞—î **–∫—ñ–ª—å–∫—ñ—Å—Ç—å –∑—É–±—Ü—ñ–≤** –Ω–∞ –∑–æ–≤–Ω—ñ—à–Ω—å–æ–º—É –∫–æ–Ω—Ç—É—Ä—ñ.")
    st.latex(r"N_{peaks} = \frac{H}{10}")
    st.write("170 —Å–º = 17 –≤–µ—Ä—à–∏–Ω.")

    # --- 6. –¢–ï–ú–ü–ï–†–ê–ú–ï–ù–¢ ---
    st.subheader("6. –¢–µ–º–ø–µ—Ä–∞–º–µ–Ω—Ç")
    st.write("–í–∏–∑–Ω–∞—á–∞—î —Å—Ç–∏–ª—å —à—Ç—Ä–∏—Ö–∞ (—Ç–æ–≤—â–∏–Ω–∞, –ø—Ä–æ–∑–æ—Ä—ñ—Å—Ç—å).")

with tab1:
    # –ü–ê–ù–ï–õ–¨ –ö–ï–†–£–í–ê–ù–ù–Ø
    st.sidebar.header("üìã –í—Ö—ñ–¥–Ω—ñ –ø–∞—Ä–∞–º–µ—Ç—Ä–∏")
    with st.sidebar:
        d = st.number_input("–î–µ–Ω—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è (–ö—ñ–ª—å–∫—ñ—Å—Ç—å –ø—Ä–æ–º–µ–Ω—ñ–≤ –∑—ñ—Ä–∫–∏)", 1, 31, 15)
        n = st.number_input("–ú—ñ—Å—è—Ü—å –Ω–∞—Ä–æ–¥–∂–µ–Ω–Ω—è (–§–æ—Ä–º–∞ –ø–µ–ª—é—Å—Ç–æ–∫)", 1, 12, 6)
        
        H = st.slider("–ó—Ä—ñ—Å—Ç (—Å–º) -> –ö—ñ–ª—å–∫—ñ—Å—Ç—å –≤–µ—Ä—à–∏–Ω –º–µ–∂—ñ", 100, 220, 170)
        
        # –ï–ù–ï–†–ì–Ü–Ø —Ç–µ–ø–µ—Ä –≤–ø–ª–∏–≤–∞—î –Ω–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –†–£–ö–ê–í–Ü–í —Å–ø—ñ—Ä–∞–ª—ñ
        T = st.slider("–ï–Ω–µ—Ä–≥—ñ—è -> –ö—ñ–ª—å–∫—ñ—Å—Ç—å –ø–æ—Ç–æ–∫—ñ–≤ —Å–ø—ñ—Ä–∞–ª–µ–π", 3, 12, 6)
        
        A = st.slider("–í—ñ–∫ (–ö—ñ–ª—å–∫—ñ—Å—Ç—å –ª—ñ–Ω—ñ–π)", 10, 100, 45)
        S = st.slider("–ì–æ–¥–∏–Ω —Å–Ω—É (–©—ñ–ª—å–Ω—ñ—Å—Ç—å —Ü–µ–Ω—Ç—Ä—É)", 0, 12, 8)
        
        st.markdown("---")
        E = st.slider("–í–ø–µ–≤–Ω–µ–Ω—ñ—Å—Ç—å (–ì–æ—Å—Ç—Ä–æ—Ç–∞ –ø–µ–ª—é—Å—Ç–æ–∫)", 0, 10, 5)
        
        st.markdown("---")
        G = st.radio("–°—Ç–∞—Ç—å", options=[1, -1], format_func=lambda x: "–ß–æ–ª–æ–≤—ñ—á–∞" if x == 1 else "–ñ—ñ–Ω–æ—á–∞")
        temp = st.selectbox("–¢–µ–º–ø–µ—Ä–∞–º–µ–Ω—Ç", options=["–°–∞–Ω–≥–≤—ñ–Ω—ñ–∫", "–•–æ–ª–µ—Ä–∏–∫", "–§–ª–µ–≥–º–∞—Ç–∏–∫", "–ú–µ–ª–∞–Ω—Ö–æ–ª—ñ–∫"])
        eye_choice = st.selectbox("–ö–æ–ª—ñ—Ä –æ—á–µ–π", options=[1, 2, 3, 4], 
                                  format_func=lambda x: {1:"–ë–ª–∞–∫–∏—Ç–Ω—ñ", 2:"–ó–µ–ª–µ–Ω—ñ", 3:"–ö–∞—Ä—ñ", 4:"–Ø–Ω—Ç–∞—Ä–Ω—ñ"}[x])

    # –ì–ï–ù–ï–†–ê–¶–Ü–Ø
    def generate_mandala():
        style_map = {
            "–°–∞–Ω–≥–≤—ñ–Ω—ñ–∫": {"lw": 1.5, "alpha": 0.7},
            "–•–æ–ª–µ—Ä–∏–∫":   {"lw": 2.5, "alpha": 0.9},
            "–§–ª–µ–≥–º–∞—Ç–∏–∫": {"lw": 4.0, "alpha": 0.3},
            "–ú–µ–ª–∞–Ω—Ö–æ–ª—ñ–∫": {"lw": 0.8, "alpha": 0.6}
        }
        s = style_map[temp]
        selected_cmap = {1: cm.winter, 2: cm.summer, 3: cm.autumn, 4: cm.spring}.get(eye_choice, cm.plasma)
        
        t = np.linspace(0, 2 * np.pi, 4000)
        fig = plt.figure(figsize=(6, 6), facecolor='black')
        ax = plt.subplot(111, projection='polar')
        ax.set_facecolor('black')
        
        SCALE = 0.12
        
        # === 1. –Ø–î–†–û (–°–û–ù) ===
        R_core = 2.0 * SCALE
        r_hole = R_core * max(0.0, 1 - (S / 12.0))
        
        ax.fill_between(t, r_hole, R_core, color=selected_cmap(0.9), alpha=s["alpha"]+0.1)
        ax.plot(t, np.full_like(t, r_hole), color='white', linewidth=0.5, alpha=0.5)

        # === 2. –ó–Ü–†–ö–ê (–î–ï–ù–¨ –ù–ê–†–û–î–ñ–ï–ù–ù–Ø d) ===
        # –ö—ñ–ª—å–∫—ñ—Å—Ç—å –ø—Ä–æ–º–µ–Ω—ñ–≤ = d
        R_layer2 = R_core + 0.3 * SCALE
        # –§–æ—Ä–º—É–ª–∞ –∑—ñ—Ä–∫–∏: cos(d * t)
        r_star = R_layer2 + 0.25 * SCALE * np.cos(d * t)
        
        ax.plot(t, r_star, color=selected_cmap(0.7), linewidth=s["lw"]*0.6, alpha=0.8)

        # === 3. –ü–ï–õ–Æ–°–¢–ö–ò (–ú–Ü–°–Ø–¶–¨ n) ===
        R_rose_base = R_layer2 + 0.5 * SCALE
        e_val = (11 - E) / 2
        r_rose = R_rose_base + (np.abs(np.cos(n/2 * t)))**e_val * 2.5 * SCALE
        
        ax.fill(t, r_rose, color=selected_cmap(0.3), alpha=0.3)
        ax.plot(t, r_rose, color=selected_cmap(0.4), linewidth=s["lw"], alpha=0.8)

        # === 4. –°–ü–Ü–†–ê–õ–Ü (–í–Ü–ö A + –ï–ù–ï–†–ì–Ü–Ø T) ===
        # –†–Ü–®–ï–ù–ù–Ø –ü–†–û–ë–õ–ï–ú–ò –ó–õ–ò–í–ê–ù–ù–Ø:
        # –ú–∏ –¥—ñ–ª–∏–º–æ –≤—Å—ñ –ª—ñ–Ω—ñ—ó –≤—ñ–∫—É –Ω–∞ "–ø—É—á–∫–∏". –ö—ñ–ª—å–∫—ñ—Å—Ç—å –ø—É—á–∫—ñ–≤ = T (–ï–Ω–µ—Ä–≥—ñ—è).
        
        max_r_rose = r_rose.max()
        num_clusters = max(3, int(T)) # –ú—ñ–Ω—ñ–º—É–º 3 —Ä—É–∫–∞–≤–∏, –º–∞–∫—Å–∏–º—É–º –∑–∞–ª–µ–∂–∏—Ç—å –≤—ñ–¥ –µ–Ω–µ—Ä–≥—ñ—ó
        
        for i in range(1, A + 1):
            progress = i / A
            
            # –í–∏–∑–Ω–∞—á–∞—î–º–æ, –¥–æ —è–∫–æ–≥–æ –ø—É—á–∫–∞ –Ω–∞–ª–µ–∂–∏—Ç—å –ª—ñ–Ω—ñ—è (–º–æ–¥—É–ª—å–Ω–∞ –∞—Ä–∏—Ñ–º–µ—Ç–∏–∫–∞)
            cluster_idx = i % num_clusters
            
            # –ë–∞–∑–æ–≤–∏–π –∫—É—Ç –ø–æ–≤–æ—Ä–æ—Ç—É –ø—É—á–∫–∞
            base_angle = cluster_idx * (2 * np.pi / num_clusters)
            
            # –ù–µ–≤–µ–ª–∏–∫–µ –∑–º—ñ—â–µ–Ω–Ω—è –≤—Å–µ—Ä–µ–¥–∏–Ω—ñ –ø—É—á–∫–∞, —â–æ–± –ª—ñ–Ω—ñ—ó –Ω–µ –Ω–∞–∫–ª–∞–¥–∞–ª–∏—Å—å —ñ–¥–µ–∞–ª—å–Ω–æ
            # –¶–µ —Å—Ç–≤–æ—Ä—é—î –µ—Ñ–µ–∫—Ç "—Ç–æ–≤—â–∏–Ω–∏" –ø–æ—Ç–æ–∫—É
            internal_offset = (i / A) * 0.2 * G 
            
            final_rotation = base_angle + internal_offset
            
            # –†–∞–¥—ñ—É—Å —Å–ø—ñ—Ä–∞–ª—ñ
            r_base = max_r_rose + progress * 2.5 * SCALE
            # –î–æ–¥–∞—î–º–æ –ª–µ–≥–∫—É —Ö–≤–∏–ª—é
            r_spiral = r_base + 0.05 * SCALE * np.sin(10*t)
            
            # –ú–∞–ª—é—î–º–æ –ª—ñ–Ω—ñ—é —Ç–æ–Ω—à–µ, —â–æ–± –Ω–µ –±—É–ª–æ "–∫–∞—à—ñ"
            ax.plot(t + final_rotation, r_spiral, 
                    color=selected_cmap(progress), 
                    linewidth=s["lw"]*0.3, # –¢–æ–Ω–∫—ñ –ª—ñ–Ω—ñ—ó
                    alpha=0.5)

        # === 5. –ú–ï–ñ–ê (–ó–†–Ü–°–¢ H) ===
        # –ß–∞—Å—Ç–æ—Ç–∞ –∑—É–±—Ü—ñ–≤ = –ó—Ä—ñ—Å—Ç / 10
        border_freq = int(H / 10)
        
        r_border_base = max_r_rose + 3.0 * SCALE
        # –§–æ—Ä–º–∞ –∑—É–±—Ü—ñ–≤ (–∑–∞–ª–µ–∂–∏—Ç—å –≤—ñ–¥ —Å—Ç–∞—Ç—ñ: –≥–æ—Å—Ç—Ä—ñ –∞–±–æ –º'—è–∫—ñ)
        p_shape = 0.5 if G == 1 else 1.5
        
        crown = (np.abs(np.sin(border_freq * t)))**p_shape
        r_border = r_border_base + (0.8 * SCALE * crown)
        
        ax.plot(t, r_border, color=selected_cmap(0.9), linewidth=s["lw"]*1.2)

        # –§—ñ–∫—Å–∞—Ü—ñ—è
        ax.set_ylim(0, 1.45)
        ax.set_axis_off()
        return fig

    col1, col2 = st.columns([1, 2])
    with col2:
        fig = generate_mandala()
        st.pyplot(fig)
        
        buf = io.BytesIO()
        fig.savefig(buf, format="png", facecolor='black', dpi=300)
        st.download_button("üì• –ó–∞–≤–∞–Ω—Ç–∞–∂–∏—Ç–∏ PNG", buf.getvalue(), "mandala.png", "image/png")
