        H = st.slider("–ó—Ä—ñ—Å—Ç (—Å–º)", 100, 220, 170)
        A = st.slider("–í—ñ–∫", 10, 100, 45)
        S = st.slider("–ì–æ–¥–∏–Ω —Å–Ω—É", 0, 12, 8)
        
        st.markdown("---")
